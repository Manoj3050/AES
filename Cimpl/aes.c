void KeyExpansion(){
    int i=0,j;
    uint8_t tmp[4];
    int k=0;
    //copy key to first 16 bytes of the Round key
    for(i=0;i<4;i++) {
        for(j=0;j<4;j++)
        roundKey[i*4+j]= Key[i*4+j];
    }
    /*
     * Round key is like in number of bytes
     * 0 4 8 12.....172
     * 1 5 7 11.....173
     * 2 6 10 14....174
     * 3 7 11 15.....175
     */
     
    // derive reound keys from the original
    for(;i<44;i++) {
        // copy previous column of round key to temporary variable
        for(j=0;j<4;j++){
            tmp[j]=roundKey[(i-1)*4+j];
        }
        // if it is next round key then shift rows of previous round key's last column and then 
        // replace with sbox values and 
        // xor with Round constant
        if(i%4==0) {
            k = tmp[0];
            tmp[0] = tmp[1];
            tmp[1] = tmp[2];
            tmp[2] = tmp[3];
            tmp[3] = k;
             
            //replace with sub bytes
            for(j=0;j<4;j++)
                tmp[j]=sbox[tmp[j]];
             
            //xor with rcon (Round constant)
            tmp[0]^=Rcon[i/4];
 
        }
        // xor prevous column of round keys with round key of 16 bytes earlier 
        for(j=0;j<4;j++){
            roundKey[(i*4)+j] = roundKey[(i-4)*4+j]^tmp[j];
        }
    }
     
}
void subBytes(){
    int i=0;
    // replace with round keys
    for(i=0;i<16;i++){
        output[i]=sbox[output[i]];
         
    }
     
}
void shiftRows(){
    uint8_t tmp;
    int i=1;
     
    // no shift in 0th row
    // 1 left shift of 1,2,3 rows
    for(i=1;i<=3;i++){
        tmp=output[i];
        output[i]  =output[i+4];
        output[i+4]=output[i+8];
        output[i+8]=output[i+12];
        output[i+12]  =tmp;
    }
    //1 left shift of 2,3 rows
    for(i=2;i<=3;i++){
        tmp=output[i];
        output[i]  =output[i+4];
        output[i+4]=output[i+8];
        output[i+8]=output[i+12];
        output[i+12]  =tmp;
    }
    // 1left shift of 3rd row
    for(i=3;i<=3;i++){
        tmp=output[i];
        output[i]  =output[i+4];
        output[i+4]=output[i+8];
        output[i+8]=output[i+12];
        output[i+12]  =tmp;
    }
 
     
     
     
}
uint8_t mult_2(uint8_t x){
    return ((x<<1)^(((x>>7)&1)*0x1b));
}
uint8_t mult_3(uint8_t x){
    return mult_2(x)^x;
}
void mixColumns(){
    int i=0;
     
    uint8_t tmp[16];
    /*
     * for mixing the column each column is multiplied by the matrix
     * 2 3 1 1
     * 1 2 3 1
     * 1 1 2 3
     * 3 1 1 2
     */
    // copy to a temprory variable
    for(i=0;i<16;i++){
        tmp[i]=output[i]; 
         
    }
    // calculate the first row after the multiply
    for(i=0;i<4;i++){
        output[i*4]=mult_2(tmp[i*4])^mult_3(tmp[i*4+1])^tmp[i*4+2]^tmp[i*4+3];
    }
    // calculate the second row after the multiply
    for(i=0;i<4;i++){
        output[i*4+1]=tmp[i*4]^mult_2(tmp[i*4+1])^mult_3(tmp[i*4+2])^tmp[i*4+3];
    }
    // calculate the third row after the multiply
    for(i=0;i<4;i++){
        output[i*4+2]=tmp[i*4]^tmp[i*4+1]^mult_2(tmp[i*4+2])^mult_3(tmp[i*4+3]);
    }
    // calculate the fourth row after the multiply
    for(i=0;i<4;i++){
        output[i*4+3]=mult_3(tmp[i*4])^tmp[i*4+1]^tmp[i*4+2]^mult_2(tmp[i*4+3]);
    }
     
                 
}
 
void addRoundKey(int round){
    int i;
    // xor with round key
    for(i=0;i<16;i++){
        output[i]^=roundKey[round*16+i];
    }
}
void encrypt(uint8_t *message){
    int r=0;
    int i=0;
     
    // copy 16 byte to output buffer array
    for(i=0;i<16;i++){
        output[i]=message[i];
    }
    /*
     * output buffer is like in number of bytes
     * 0 4 8 12
     * 1 5 9 13
     * 2 6 10 14
     * 3 7 11 15
     * contained in continues array
     */
    // add first round key
    addRoundKey(0);
    // rounds 1 to 9
    for(r=1;r<10;r++){
        subBytes();
        shiftRows();
        mixColumns();
        addRoundKey(r);
    }
    // final round- does not contain mix columns step
    subBytes();
    shiftRows();
    addRoundKey(10);
     
    // copy the output to the main output buffer
    for(i=0;i<16;i++){
        output_buf[buf_counter]=output[i];
        buf_counter++;
    }
     
     
}
